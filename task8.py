k = int(input("Введите день года: "))
if k % 7 == 0:
    print("понедельник")
elif (k - 1) % 7 == 0:
    print("вторник")
elif (k - 2) % 7 == 0:
    print("среда")
elif (k - 3) % 7 == 0:
    print("четверг")
elif (k - 4) % 7 == 0:
    print("пятница")
elif (k - 5) % 7 == 0:
    print("суббота")
elif (k - 6) % 7 == 0:
    print("воскресенье4")




